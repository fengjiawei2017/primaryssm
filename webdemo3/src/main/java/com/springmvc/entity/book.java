package com.springmvc.entity;

public class book {

	String bname;

	int bid;
	String bauthor;
	int bcount;
	String bpublisher;
	int bprices;
	String bpicture;
	String bnice;

	public String getBname() {
		return bname;
	}

	public void setBname(String bname) {
		this.bname = bname;
	}

	public int getBid() {
		return bid;
	}

	public void setBid(int bid) {
		this.bid = bid;
	}

	public String getBauthor() {
		return bauthor;
	}

	public void setBauthor(String bauthor) {
		this.bauthor = bauthor;
	}

	public int getBcount() {
		return bcount;
	}

	public void setBcount(int bcount) {
		this.bcount = bcount;
	}

	public String getBpublisher() {
		return bpublisher;
	}

	public void setBpublisher(String bpublisher) {
		this.bpublisher = bpublisher;
	}

	public int getBprices() {
		return bprices;
	}

	public void setBprices(int bprices) {
		this.bprices = bprices;
	}

	public String getBpicture() {
		return bpicture;
	}

	public void setBpicture(String bpicture) {
		this.bpicture = bpicture;
	}

	public String getBnice() {
		return bnice;
	}

	public void setBnice(String bnice) {
		this.bnice = bnice;
	}

}
